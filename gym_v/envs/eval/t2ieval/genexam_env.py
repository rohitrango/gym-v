PROMPT_FOR_EVAL = """
You are an expert-level evaluator for multi-disciplinary text-to-image generation. You will be given an input prompt for text-to-image generation, an image generated by a model (the first image), a ground truth image for reference (the second image), and several scoring point questions and their corresponding scores. Your task is to evaluate the generated image by answering the scoring point questions and evaluating the image from some global perspectives.

## Instructions
1. Remember that the first image is the generated image from a model, and the second image is the ground truth image. You should **only evaluate the generated image**, while the ground truth image can be used for reference.
2. First, give a detailed description of all the components in the generated image.
3. For each scoring point, you should use the ground truth image as **reference information** to make more accurate judgments. You should generate a **detailed** reasoning **step by step**. It should first analyze the question, what subject knowledge it requires (some questions may not explicitly include all the knowledge-related details, e.g. the shape of musical signs, the structure of molecules, etc.) and what information you need to refer to from the ground truth image, then analyze whether the generated image satisfies the scoring point based on the scoring point, prompt and reference information. Finally, answer 1 if the generated image fully satisfies the scoring point, otherwise answer 0. For example, if a scoring point is "Does the cell membrane label correspond to the correct position in the cell?", you can refer to the ground truth image to check the correct position of the cell membrane label as an example, by **explicitly analyzing the ground truth image** in your reasoning.
4. The answer is only based on the information in the generated image. Do not implicate or reason about more information, e.g. If the question is "Is the force 10N?", and the generated image does not mark the magnitude of the force, the answer should be 0. For some information (e.g. angle, length, perpendicular, etc.), you should not only rely on explicit marks in the image (if any), but also analyze the image itself, e.g. if the question is "Is the angle 90 degrees?", you should not only check the text label, but also analyze the degree of the angle based on the image.
5. **Do not focus too much or be too strict on the text labels.** If some information is not explicitly marked or labeled in the image, you should analyze the image itself to check the information, e.g. whether the value or relationships of angles, segments, etc.  For instance, do not judge "AB = AC" as incorrect because the length of AB and AC is not marked in the image.
6. When checking information quantitative relationships like angles, lengths, coordinates, etc. in the image, do not be too strict. Only a rough fulfillment is required, and minor drawing errors are allowed.
7. You should also evaluate the generated image from some global perspectives provided below. For each perspective, first provide a **detailed step-by-step** reasoning, e.g. recognize all the text labels for spelling, then give a score in the defined range. When checking these global perspectives, you should check the generated image only and **do not check its consistency with the input prompt**.

## Global Perspectives
1. Spelling (range: 0-2): The spelling of the text in the image, including the notations and equations. You should first recognize the text in the image in the reasoning, then check the spelling of the text. This should not be affected by the resolution of the image  (e.g. the image is blurry), unless key information is not identifiable. Specifically:
- 0: There are critical errors in spelling, notations or equations which significantly hinders the understanding of the image.
- 1: There are some errors in spelling, notations or equations that somehow hinder the understanding of key information in the image.
- 2: All or almost all the spelling, notations and equations are correct. Tiny errors like spaces, commas, capital letters, etc. are allowed.
2. Clarity and Readability (range: 0-2): The clarity and readability of the image. Each component in the image should be clearly readable and identifiable. All text labels and marks should in the right place and are not overlapped or occluded by other elements. If the image is geometry or diagram, there should not be unlabeled points or lines or duplicated labels. If the image is a plot or chart, the axis should be labeled and the ticks should be carefully checked. You should first identify the components, labels, and marks in the image in the reasoning, then check their clarity and readability. This should not be affected by the resolution of the image  (e.g. the image is blurry), unless key information is not identifiable. Specifically:
- 0: There are critical clarity and readability issues which significantly hinders the understanding of the image e.g. some components are impossible to distinguish, or some labels are fully overlapped or occluded by other elements, or many key information are not labeled.
- 1: There are some clarity and readability issues, e.g. some components are not clearly readable and identifiable, or some labels are overlapped or occluded, or some key information are not labeled.
- 2: The clarity and readability is perfect or almost perfect. Components are clearly readable and identifiable and necessary labels and marks are present. Tiny errors are allowed.
3. Logical Consistency (range: 0-2): The logical consistency of the image. Check the correctness of all the marks, text, musical notes, etc. If the image is geometry, check the correctness of each marked angles, lengths, coordinates, etc. If the image is a plot or chart, check the correctness of each data point, the axis, the ticks, the legend, etc. You should first identify the components, labels, and marks in the image in the reasoning, then check their logical consistency. This should not be affected by the resolution of the image (e.g. the image is blurry), unless key information is not identifiable. Specifically:
- 0: There are critical logical consistency issues which significantly hinders the understanding of the image, e.g. some key marks are not correct, some angles/lengths/data points are significantly inconsistent with the text label, etc.
- 1: There are some logical consistency issues that somehow hinder the understanding of key information in the image, e.g. some marks are not correct, some angles/lengths/data points are inconsistent with the text label, etc.
- 2: The logical consistency is perfect or almost perfect. Marks, text, musical notes, etc. are correct and consistent. Tiny errors are allowed.

## Output Format
Respond STRICTLY in the following format:
{{
    "description": "...",
    "answers": [
        {{
            "reasoning": "...",
            "answer": 1
        }},
        {{
            "reasoning": "...",
            "answer": 0
        }},
        ...
    ],
    "global_evaluation": {{
        "Spelling": {{
            "reasoning": "...",
            "score": 0,
        }},
        "Clarity and Readability": {{
            "reasoning": "...",
            "score": 0,
        }},
        "Logical Consistency": {{
            "reasoning": "...",
            "score": 0,
        }},
    }}
}}

Make sure your response follows the format strictly and can be parsed by Python json.loads. Do not include any other explanation or formatting.

## Prompt
{prompt}

## Scoring Points
{scoring_points}
"""

def resize_image(img, target_size=1024):
    if target_size is not None and target_size > 0:
        w, h = img.size
        if max(w, h) > target_size:
            if w >= h:
                new_w = target_size
                new_h = int(h * target_size / w)
            else:
                new_h = target_size
                new_w = int(w * target_size / h)
            img = img.resize((new_w, new_h), Image.LANCZOS)
            
    return img


class GenExamPromptDataset(Dataset):
    def __init__(
        self,
        dataset_root: str | None = None,
        file_path: str | None = None,
    ):
        if file_path is None and dataset_root and dataset_root.endswith(".jsonl"):
            file_path = dataset_root
        if file_path is None:
            file_path = os.path.join(dataset_root, "annotations", "All_Subjects.jsonl")

        self.file_path = file_path
        with open(self.file_path, encoding="utf-8") as f:
            self.metadatas = [json.loads(line) for line in f]
        self.prompts = [item.get("prompt", "") for item in self.metadatas]

    def __len__(self):
        return len(self.prompts)

    def __getitem__(self, idx):
        return {"prompt": self.prompts[idx], "metadata": self.metadatas[idx]}

    @staticmethod
    def collate_fn(examples):
        prompts = [example["prompt"] for example in examples]
        metadatas = [example["metadata"] for example in examples]
        return prompts, metadatas


def _compute_genexam_scores(
    result: dict[str, Any], scoring_points: list[dict[str, Any]]
):
    scoring_point_scores = 0.0
    answers = result["answers"]
    for idx, answer in enumerate(answers):
        if answer.get("answer") == 1:
            scoring_point_scores += scoring_points[idx]["score"]

    total_score = round(sum(item["score"] for item in scoring_points), 4)
    assert total_score == 1, f"Invalid sum of scoring point scores: {total_score}"

    global_eval = result["global_evaluation"]
    spelling = global_eval["Spelling"]["score"]
    readability = global_eval["Clarity and Readability"]["score"]
    logical_consistency = global_eval["Logical Consistency"]["score"]

    semantic_correctness = round(scoring_point_scores, 4)
    scoring_point_all_correct = all(item.get("answer") == 1 for item in answers)

    strict_score = int(
        scoring_point_all_correct
        and spelling == 2
        and readability == 2
        and logical_consistency == 2
    )
    relaxed_score = round(
        semantic_correctness * 0.7
        + spelling * 0.1 / 2
        + readability * 0.1 / 2
        + logical_consistency * 0.1 / 2,
        3,
    )

    return {
        "semantic_correctness": semantic_correctness,
        "spelling": spelling,
        "readability": readability,
        "logical_consistency": logical_consistency,
        "scoring_point_all_correct": scoring_point_all_correct,
        "strict_score": strict_score,
        "relaxed_score": relaxed_score,
    }


def _preprocess_genexam_payload(
    *,
    model: str,
    prompt_text: str,
    gen_image: Image.Image,
    gt_image: Image.Image,
    max_tokens: int,
) -> dict[str, Any]:
    gen_url = convert_local_image_or_video_to_base_64(
        gen_image, media_type="image", image_format="JPEG"
    )
    gt_url = convert_local_image_or_video_to_base_64(
        gt_image, media_type="image", image_format="JPEG"
    )
    return {
        "model": model,
        "messages": [
            {
                "role": "user",
                "content": [
                    {"type": "text", "text": prompt_text},
                    {
                        "type": "image_url",
                        "image_url": {"url": gen_url, "detail": "high"},
                    },
                    {
                        "type": "image_url",
                        "image_url": {"url": gt_url, "detail": "high"},
                    },
                ],
            }
        ],
        "stream": False,
        "reasoning_effort": "low",
        "verbosity": "medium",
        "max_completion_tokens": max_tokens,
    }


def _preprocess_genexam_inputs(
    *,
    action: Any,
    metadata: dict[str, Any],
    dataset_root: str | None,
    image_max_size: int,
) -> tuple[Image.Image, Image.Image, str, list[dict[str, Any]]]:
    gen_image = convert_base64_to_local_image_or_video(
        action["image"], media_type="image"
    )

    gt_path = metadata["image_path"]
    gt_path = os.path.join(dataset_root, "images", gt_path)

    with Image.open(gt_path) as gt_img:
        gt_img = gt_img.convert("RGB")

    gen_image = resize_image(gen_image, image_max_size)
    gt_img = resize_image(gt_img, image_max_size)

    scoring_points = metadata.get("scoring_points")
    scoring_questions = [item["question"] for item in scoring_points]
    prompt_text = PROMPT_FOR_EVAL.format(
        prompt=metadata.get("prompt"),
        scoring_points=scoring_questions,
    )

    return gen_image, gt_img, prompt_text, scoring_points


def _postprocess_genexam_results(
    contents: list[str],
    scoring_points_list: list[list[dict[str, Any]]],
) -> tuple[list[dict[str, Any]], list[str | None]]:
    scores_list: list[dict[str, Any]] = []
    errors: list[str | None] = []
    for content, scoring_points in zip(contents, scoring_points_list, strict=False):
        response_obj: dict[str, Any] | None = None
        text = content.split("```json")[-1].split("```")[0]
        try:
            response_obj = json.loads(text)
        except Exception:
            try:
                response_obj = ast.literal_eval(text)
            except Exception:
                response_obj = None

        if response_obj is None:
            scores_list.append({})
            errors.append("Failed to parse JSON response")
            continue

        try:
            assert (
                "global_evaluation" in response_obj and "answers" in response_obj
            ), f"Invalid response: {response_obj}"
            assert len(response_obj["answers"]) == len(
                scoring_points
            ), f"Invalid number of answers: {len(response_obj['answers'])}"
            assert all(
                item["answer"] in [0, 1] for item in response_obj["answers"]
            ), f"Invalid answer: {response_obj['answers']}"
            assert (
                "Clarity and Readability" in response_obj["global_evaluation"]
                and "Logical Consistency" in response_obj["global_evaluation"]
                and "Spelling" in response_obj["global_evaluation"]
            ), f"Invalid global evaluation: {response_obj['global_evaluation']}"
        except Exception as exc:
            scores_list.append({})
            errors.append(str(exc))
            continue

        scores_list.append(_compute_genexam_scores(response_obj, scoring_points))
        errors.append(None)
    return scores_list, errors


def genexam_score_async(
    *,
    base_url: str,
    api_key: str,
    model: str,
    max_tokens: int,
    parse_retries: int = 3,
    timeout_s: float = 120.0,
    max_retries: int = 3,
    backoff_factor: float = 1.0,
    max_concurrency: int | None = None,
):
    client = BaseNetworkClient(
        endpoint=f"{base_url}/chat/completions",
        headers={
            "Content-Type": "application/json",
            "Authorization": f"Bearer {api_key}",
        },
        timeout_s=timeout_s,
        max_retries=max_retries,
        backoff_factor=backoff_factor,
        concurrency=1 if max_concurrency is None else max_concurrency,
    )

    async def _fn(image_triplets, scoring_points_list):
        payloads = [
            _preprocess_genexam_payload(
                model=model,
                prompt_text=prompt_text,
                gen_image=gen_image,
                gt_image=gt_image,
                max_tokens=max_tokens,
            )
            for gen_image, gt_image, prompt_text in image_triplets
        ]

        responses = await client.request_many(payloads)
        contents: list[str] = []
        for response in responses:
            content = response["choices"][0]["message"]["content"]
            contents.append(content)
        scores_list, errors = _postprocess_genexam_results(
            contents, scoring_points_list
        )

        attempts = max(int(parse_retries), 1)
        attempt = 1
        while attempt < attempts and any(errors):
            retry_indices = [idx for idx, err in enumerate(errors) if err]
            retry_payloads = [payloads[idx] for idx in retry_indices]
            retry_responses = await client.request_many(retry_payloads)
            retry_contents: list[str] = []
            for response in retry_responses:
                content = response["choices"][0]["message"]["content"]
                retry_contents.append(content)
            for idx, content in zip(retry_indices, retry_contents, strict=False):
                contents[idx] = content
            scores_list, errors = _postprocess_genexam_results(
                contents, scoring_points_list
            )
            attempt += 1

        if any(errors):
            raise ValueError(
                f"GenExam parse failed after {attempts} attempt(s): {errors}"
            )

        return scores_list, errors

    return _fn


class GenExamEnv(Env):
    """
    T2I eval environment for GenExam (GPT judge).

    reset(): returns prompts + metadata for all items (or subset).
    step(): expects images as actions, queries GPT directly.
    """

    def __init__(
        self,
        dataset_root: str | None = None,
        dataset_path: str | None = None,
        model: str | None = "gpt-5-2025-08-07",
        reward_mode: str = "relaxed",
        max_tokens: int = 16384,
        image_max_size: int = 768,
        parse_retries: int = 3,
        timeout_s: float = 120.0,
        max_retries: int = 3,
        backoff_factor: float = 1.0,
        max_concurrency: int | None = None,
        **kwargs: Any,
    ):
        super().__init__(max_episode_steps=1)

        openai_base_url = os.environ.get("OPENAI_BASE_URL")
        openai_api_key = os.environ.get("OPENAI_API_KEY")
        if dataset_root is None and dataset_path is None:
            raise ValueError(
                "GenExam requires dataset_root or dataset_path."
            )
        if dataset_root is None and dataset_path is not None:
            parent_dir = os.path.dirname(os.path.abspath(dataset_path))
            if os.path.basename(parent_dir) == "annotations":
                dataset_root = os.path.dirname(parent_dir)
            else:
                dataset_root = parent_dir

        self.dataset_root = dataset_root
        self.dataset = GenExamPromptDataset(
            dataset_root=dataset_root, file_path=dataset_path
        )
        self.prompts = self.dataset.prompts
        self.metadatas = self.dataset.metadatas
        self.indices = list(range(len(self.prompts)))

        self._agent_ids = {f"agent_{i}" for i in self.indices}
        self._active_indices = self.indices

        if not openai_base_url or not openai_api_key:
            raise ValueError(
                "GenExam requires OPENAI_BASE_URL and OPENAI_API_KEY "
                "environment variables."
            )

        self.reward_mode = reward_mode
        self.max_tokens = max_tokens
        self.image_max_size = image_max_size
        self.max_concurrency = max_concurrency

        self._model = model
        self._score_async_fn = genexam_score_async(
            base_url=openai_base_url,
            api_key=openai_api_key,
            model=self._model,
            max_tokens=self.max_tokens,
            parse_retries=parse_retries,
            timeout_s=timeout_s,
            max_retries=max_retries,
            backoff_factor=backoff_factor,
            max_concurrency=self.max_concurrency,
        )

    @property
    def description(self) -> str:
        return f"GenExam T2I eval env with {len(self.prompts)} prompts."

    def reset(self, *, seed=None, options=None):
        super().reset(seed=seed, options=options)

        indices = select_indices(self.indices, options, self.np_random)

        self._active_indices = indices
        self._agent_ids = {f"agent_{i}" for i in indices}

        obs_dict = {}
        info_dict = {}
        for idx in indices:
            agent_id = f"agent_{idx}"
            prompt = self.prompts[idx]
            metadata = self.metadatas[idx]
            obs_dict[agent_id] = Observation(image=None, text=prompt, metadata=metadata)
            info_dict[agent_id] = {
                "index": idx,
                "prompt": prompt,
                "metadata": metadata,
            }

        return obs_dict, info_dict

    def step(self, action_dict):
        agent_ids = list(action_dict.keys())

        image_triplets = []
        scoring_points_list = []
        indices = []
        for agent_id in agent_ids:
            action = action_dict[agent_id]
            idx = int(agent_id.split("_")[-1])
            indices.append(idx)
            metadata = self.metadatas[idx]
            gen_image, gt_img, prompt_text, scoring_points = _preprocess_genexam_inputs(
                action=action,
                metadata=metadata,
                dataset_root=self.dataset_root,
                image_max_size=self.image_max_size,
            )
            image_triplets.append((gen_image, gt_img, prompt_text))
            scoring_points_list.append(scoring_points)

        parsed_scores, parse_errors = run_coroutine(
            self._score_async_fn(image_triplets, scoring_points_list)
        )

        reward_dict = {}
        info_dict = {}
        for idx, agent_id in enumerate(agent_ids):
            scoring_points = scoring_points_list[idx]
            scores = parsed_scores[idx]
            error = parse_errors[idx]

            if not scores:
                scores = {
                    "semantic_correctness": 0.0,
                    "spelling": 0,
                    "readability": 0,
                    "logical_consistency": 0,
                    "strict_score": 0,
                    "relaxed_score": 0.0,
                }

            reward = (
                scores["strict_score"]
                if self.reward_mode == "strict"
                else scores["relaxed_score"]
            )
            reward_dict[agent_id] = float(reward)
            info = {
                "index": indices[idx],
                "strict_score": scores["strict_score"],
                "relaxed_score": scores["relaxed_score"],
                "semantic_correctness": scores["semantic_correctness"],
                "spelling": scores["spelling"],
                "readability": scores["readability"],
                "logical_consistency": scores["logical_consistency"],
            }
            if error:
                info["error"] = error
            info_dict[agent_id] = info

        terminated = {agent_id: True for agent_id in agent_ids}
        truncated = {agent_id: False for agent_id in agent_ids}
        terminated["__all__"] = True
        truncated["__all__"] = False

        return {}, reward_dict, terminated, truncated, info_dict
